// 1. Google'ın aradığı ana fonksiyon (Dışarıda olmalı!)
function googleTranslateElementInit() {
    new google.translate.TranslateElement({
        pageLanguage: 'tr',
        layout: google.translate.TranslateElement.InlineLayout.SIMPLE,
        autoDisplay: false
    }, 'google_translate_element');
}

function translateTo(lang) {
    const selectField = document.querySelector("#google_translate_element select");
    
    if (selectField) {
        // Eğer servis hazırsa çeviriyi yap
        selectField.value = lang;
        selectField.dispatchEvent(new Event('change'));
    } else {
        // Eğer servis hazır değilse kullanıcıya uyarı çıkar
        alert("Şu anda çeviri servisine bağlanılamıyor. Lütfen birkaç saniye sonra tekrar deneyiniz veya internet bağlantınızı kontrol ediniz.");
        
        // Konsola teknik detay yaz (senin görmen için)
        console.warn("Google Translate Element bulunamadı. Script yüklenmemiş olabilir.");
    }
}

function akilliGeriDon() {
    const neredenGeldim = document.referrer; // Bir önceki sayfanın tam adresi

    // Eğer bir önceki sayfa ana sayfa (index.html) ise veya referrer boşsa (direkt giriş)
    if (neredenGeldim.includes('index.html') || neredenGeldim === "" || neredenGeldim.endsWith('/')) {
        window.location.href = 'DevamEdenProjeler.html'; 
    } else {
        // Eğer kullanıcı zaten listeleme sayfalarından birinden geldiyse kaldığı yere döner
        window.history.back();
    }
}